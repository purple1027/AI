package javaBasic03.ch06.sec8;

public class Printer {
	
	void println (int n) {
	System.out.println(n);	
		
	}
	
	void println (boolean b) {
		System.out.println(b);
	
		}
	void println (double d) {
		System.out.println(d);	
			
		}
	
	void println (String s) {
		System.out.println(s);	
		}
	
	
} //class
